import java.util.*;
public class Swapnowithvaria {

    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter A:");
        int a=sc.nextInt();
        System.out.print("Enter B:");
        int b=sc.nextInt();
        int c=a;
        a=b;
        b=c;
        System.out.println(a);
        System.out.println(b);
        sc.close();
    }
}

    
        
    

